# Primitive Data Types
name = "Alice"             # String
age = 30                   # Integer
height = 5.7               # Float
is_student = False         # Boolean

# Non-Primitive Data Types
hobbies = ["reading", "gaming", "hiking"]     # List
grades = {"Math": 90, "Science": 85}          # Dictionary
unique_ids = {101, 102, 103}                  # Set
coordinates = (34.05, -118.25)                # Tuple

# Display the data
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)
print("Hobbies:", hobbies)
print("Grades:", grades)
print("Unique IDs:", unique_ids)
print("Coordinates:", coordinates)
