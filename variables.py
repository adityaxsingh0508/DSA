# Variable creation and assignment
x = 10               # integer
name = "Alice"       # string
price = 19.99        # float
is_valid = True      # boolean

# Displaying variable values
print("x =", x)
print("name =", name)
print("price =", price)
print("is_valid =", is_valid)

# Checking data types
print("Type of x:", type(x))
print("Type of name:", type(name))
print("Type of price:", type(price))
print("Type of is_valid:", type(is_valid))

# Multiple assignment
a, b, c = 1, 2, 3
print("a + b + c =", a + b + c)

# Dynamic typing
x = "Now I'm a string!"
print("x =", x)
print("Type of x after change:", type(x))

# Variable naming rules (valid examples)
student_name = "John"
_student_id = 123
age2 = 20

print("student_name:", student_name)
print("_student_id:", _student_id)
print("age2:", age2)