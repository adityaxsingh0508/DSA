
# For loop example
print("For loop:")
for i in range(1, 6):
    print(i)

# While loop example
print("\nWhile loop:")
i = 1
while i <= 5:
    print(i)
    i += 1

# For loop with break
print("\nFor loop with break:")
for i in range(1, 10):
    if i == 6:
        break
    print(i)

# For loop with continue
print("\nFor loop with continue:")
for i in range(1, 6):
    if i == 3:
        continue
    print(i)



